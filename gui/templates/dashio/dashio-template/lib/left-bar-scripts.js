/*---LEFT BAR ACCORDION----*/
$(function() {
  $('#nav-accordion').dcAccordion({
    eventType: 'click',
    autoClose: true,
    saveState: true,
    disableLink: true,
    speed: 'slow',
    showCount: false,
    autoExpand: true,
    classExpand: 'dcjq-current-parent'
  });
});

var Script = function() {

  // sidebar dropdown menu auto scrolling
  jQuery('#sidebar .sub-menu > a').click(function() {
    var o = ($(this).offset());
    diff = 250 - o.top;

    // alert('DEBUG LINE 23-left-bar-scripts.js' + $("#sidebar").attr('id'));

    if (diff > 0)
      $("#sidebar").scrollTo("-=" + Math.abs(diff), 500);
    else
      $("#sidebar").scrollTo("+=" + Math.abs(diff), 500);
  });



  //    sidebar toggle
  $(function() {
    function responsiveView() {
      var wSize = $(window).width();
      if (wSize <= 768) {
        $('#container').addClass('sidebar-close');
        $('#sidebar > ul').hide();
      }

      if (wSize > 768) {
        $('#container').removeClass('sidebar-close');
        $('#sidebar > ul').show();
      }
    }
    $(window).on('load', responsiveView);
    $(window).on('resize', responsiveView);
  });

  /**
   *
   */
  $('.fa-bars').click(function() {

    var sidebar = window.parent.frames[1].document.getElementById('sidebar');
    var sidebarUL = window.parent.frames[1].document.getElementById('nav-accordion');
    var mainContent = window.parent.frames[1].document.getElementById('main-content');
    var container = window.parent.frames[1].document.getElementById('container');


    var JQsidebar = $(sidebar);
    var JQsidebarUL = $(sidebarUL);
    var JQmainContent = $(mainContent);
    var JQcontainer = $(container);


    //alert(sidebarUL);
    if (JQsidebarUL.is(":visible") === true) {
      JQmainContent.css({
        'margin-left': '0px'
      });
      JQsidebar.css({
        'margin-left': '-250px'
      });
      JQsidebarUL.hide();
      JQcontainer.addClass("sidebar-closed");
    } else {
      JQmainContent.css({
        'margin-left': '250px'
      });
      JQsidebarUL.show();
      JQsidebar.css({
        'margin-left': '0'
      });
      JQcontainer.removeClass("sidebar-closed");
    }

  });

  // custom scrollbar
  $("#sidebar").niceScroll({
    styler: "fb",
    cursorcolor: "#4ECDC4",
    cursorwidth: '3',
    cursorborderradius: '10px',
    background: '#404040',
    spacebarenabled: false,
    cursorborder: ''
  });

  //  $("html").niceScroll({styler:"fb",cursorcolor:"#4ECDC4", cursorwidth: '6', cursorborderradius: '10px', background: '#404040', spacebarenabled:false,  cursorborder: '', zindex: '1000'});

  // widget tools

  jQuery('.panel .tools .fa-chevron-down').click(function() {
    var el = jQuery(this).parents(".panel").children(".panel-body");
    if (jQuery(this).hasClass("fa-chevron-down")) {
      jQuery(this).removeClass("fa-chevron-down").addClass("fa-chevron-up");
      el.slideUp(200);
    } else {
      jQuery(this).removeClass("fa-chevron-up").addClass("fa-chevron-down");
      el.slideDown(200);
    }
  });

  jQuery('.panel .tools .fa-times').click(function() {
    jQuery(this).parents(".panel").parent().remove();
  });


  //    tool tips

  $('.tooltips').tooltip();

  //    popovers

  $('.popovers').popover();



  // custom bar chart

  if ($(".custom-bar-chart")) {
    $(".bar").each(function() {
      var i = $(this).find(".value").html();
      $(this).find(".value").html("");
      $(this).find(".value").animate({
        height: i
      }, 2000)
    })
  }

}();

jQuery(document).ready(function( $ ) {

  // Go to top
  $('.go-top').on('click', function(e) {
    e.preventDefault();
    $('html, body').animate({scrollTop : 0},500);
  });
});
